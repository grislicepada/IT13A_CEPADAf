    public users(String voterId) {
        this.voterId = voterId;
        initComponents();
        
        try {
            BufferedReader reader = new BufferedReader(new FileReader("C:\\Users\\IMO\\Documents\\grisliCodes\\votersList.txt"));
            StringBuilder content = new StringBuilder();
            String line; //each line is added to a string builder.

            while ((line = reader.readLine()) != null) { //ma read sya as long as ang line is dili puno.
                content.append(line).append("\n");
            }

            reader.close();

            votersListPane.setText(content.toString()); // âœ… uses your JTextPane name ug ma display ang file contents in the GUI.
            votersListPane.setEditable(false); // optional: makes it read-only

        } catch (Exception e) {
            e.printStackTrace();
            votersListPane.setText("Error loading voter list.");
        }

    }

